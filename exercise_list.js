// Helper functions for filtering exercises
const getExercisesByType = (type) => {
    const filtered = exerciseList.filter(exercise => exercise.type === type);
    console.log(`${type} exercises:`, filtered);
    return filtered;
}

const getExercisesByWeight = (weightType) => {
    const filtered = exerciseList.filter(exercise => exercise.weight.includes(weightType));
    console.log(`${weightType} exercises:`, filtered);
    return filtered;
}

const getRandomExercise = (list) => {
    const randomIndex = Math.floor(Math.random() * list.length);
    const exercise = list[randomIndex];
    console.log('Random exercise:', exercise);
    return exercise;
}

